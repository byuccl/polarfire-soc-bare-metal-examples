uint16_t
get_ptp_packet_short_uint(uint8_t *source)
{
    uint16_t ret_val;

    ret_val = (uint16_t)(*source << 16);
    source++;
    ret_val = (uint16_t)(ret_val + (uint16_t)(*source));

    return (ret_val);
}

uint32_t
get_ptp_packet_uint(uint8_t *source)
{
    uint32_t ret_val = 0;
    int count;

    for (count = 0; count != 4; count++)
    {
        ret_val <<= 8;
        ret_val += (uint32_t)*source;
        source++;
    }

    return (ret_val);
}

